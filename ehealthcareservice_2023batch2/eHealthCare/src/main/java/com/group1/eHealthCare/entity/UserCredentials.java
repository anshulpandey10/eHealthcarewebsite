package com.group1.eHealthCare.entity;

public class UserCredentials {
	private String inputusernameString;
	private String inputpasswordString;

	// Simple POJO class to represent user credential (username and password).
	// Getters and Setters are provided for the attributes.
	public String getInputusernameString() {
		return inputusernameString;
	}

	public void setInputusernameString(String inputusernameString) {
		this.inputusernameString = inputusernameString;
	}

	public String getInputpasswordString() {
		return inputpasswordString;
	}

	public void setInputpasswordString(String inputpasswordString) {
		this.inputpasswordString = inputpasswordString;
	}

}
